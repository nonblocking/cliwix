package at.nonblocking.cliwix.model;

import javax.xml.bind.annotation.XmlAccessType;
import javax.xml.bind.annotation.XmlAccessorType;
import javax.xml.bind.annotation.XmlElement;
import javax.xml.bind.annotation.XmlRootElement;
import java.util.List;

@XmlRootElement(name = "Organizations")
@XmlAccessorType(XmlAccessType.FIELD)
public class Organizations extends TreeType<Organization> {

    @XmlElement(name = "Organization", required = true)
    private List<Organization> organizations;

    public Organizations() {
    }

    public Organizations(List<Organization> organizations) {
        this.organizations = organizations;
    }

    public List<Organization> getOrganizations() {
        return organizations;
    }

    public void setOrganizations(List<Organization> organizations) {
        this.organizations = organizations;
    }

    @Override
    public List<Organization> getRootItems() {
        return this.organizations;
    }

    @Override
    public List<Organization> getSubItems(Organization organization) {
        return organization.getSubOrganizations();
    }
}
